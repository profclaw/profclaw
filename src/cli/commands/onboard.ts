/**
 * CLI Onboard Command
 *
 * Zero-to-running in one command. Detects environment, recommends deployment mode,
 * generates .env, runs setup wizard, starts server.
 *
 * Usage:
 *   profclaw onboard                                    # Interactive
 *   profclaw onboard --non-interactive --mode mini      # CI/Docker
 */

import { Command } from 'commander';
import chalk from 'chalk';
import figlet from 'figlet';
import { createInterface } from 'readline/promises';
import { existsSync, writeFileSync, readFileSync } from 'node:fs';
import { join } from 'node:path';
import { execSync } from 'node:child_process';
import { success, error, info, warn, spinner } from '../utils/output.js';
import type { DeploymentMode } from '../../core/deployment.js';

// ---------------------------------------------------------------------------
// Environment Detection
// ---------------------------------------------------------------------------

interface EnvironmentInfo {
  os: string;
  arch: string;
  nodeVersion: string;
  totalMemoryMb: number;
  isDocker: boolean;
  hasRedis: boolean;
  hasOllama: boolean;
  hasClaude: boolean;
  hasGit: boolean;
  existingEnv: boolean;
  existingDb: boolean;
}

async function detectEnvironment(): Promise<EnvironmentInfo> {
  const os = await import('node:os');

  const isDocker = existsSync('/.dockerenv') || existsSync('/run/.containerenv');
  const totalMemoryMb = Math.round(os.totalmem() / (1024 * 1024));

  let hasRedis = false;
  try {
    const IORedis = (await import('ioredis')).default;
    const redisUrl = process.env.REDIS_URL || 'redis://localhost:6379';
    const redis = new IORedis(redisUrl, {
      maxRetriesPerRequest: 1,
      connectTimeout: 2000,
      lazyConnect: true,
    });
    await redis.connect();
    await redis.ping();
    await redis.disconnect();
    hasRedis = true;
  } catch { /* no redis */ }

  let hasOllama = false;
  try {
    const url = process.env.OLLAMA_BASE_URL || 'http://localhost:11434';
    const res = await fetch(`${url}/api/tags`, { signal: AbortSignal.timeout(2000) });
    hasOllama = res.ok;
  } catch { /* no ollama */ }

  let hasClaude = false;
  try {
    execSync('which claude', { stdio: 'ignore' });
    hasClaude = true;
  } catch { /* no claude */ }

  let hasGit = false;
  try {
    execSync('which git', { stdio: 'ignore' });
    hasGit = true;
  } catch { /* no git */ }

  return {
    os: `${os.platform()} ${os.release()}`,
    arch: os.arch(),
    nodeVersion: process.version,
    totalMemoryMb,
    isDocker,
    hasRedis,
    hasOllama,
    hasClaude,
    hasGit,
    existingEnv: existsSync(join(process.cwd(), '.env')),
    existingDb: existsSync(join(process.cwd(), 'data', 'profclaw.db')),
  };
}

function recommendMode(env: EnvironmentInfo): DeploymentMode {
  // Low memory or no Redis: pico
  if (env.totalMemoryMb < 512) return 'pico';

  // Has Redis and plenty of RAM: pro
  if (env.hasRedis && env.totalMemoryMb >= 2048) return 'pro';

  // Default: mini
  return 'mini';
}

// ---------------------------------------------------------------------------
// .env Generation
// ---------------------------------------------------------------------------

function generateEnvFile(opts: {
  mode: DeploymentMode;
  anthropicKey?: string;
  openaiKey?: string;
  ollamaUrl?: string;
  port?: string;
}): string {
  const lines: string[] = [
    '# profClaw Configuration',
    `# Generated by: profclaw onboard`,
    `# Date: ${new Date().toISOString()}`,
    '',
    '# Deployment Mode',
    `PROFCLAW_MODE=${opts.mode}`,
    '',
    `PORT=${opts.port || '3000'}`,
    '',
  ];

  if (opts.anthropicKey) {
    lines.push('# AI Provider');
    lines.push(`ANTHROPIC_API_KEY=${opts.anthropicKey}`);
    lines.push('');
  } else if (opts.openaiKey) {
    lines.push('# AI Provider');
    lines.push(`OPENAI_API_KEY=${opts.openaiKey}`);
    lines.push('');
  } else if (opts.ollamaUrl) {
    lines.push('# AI Provider (Ollama - free local AI)');
    lines.push(`OLLAMA_BASE_URL=${opts.ollamaUrl}`);
    lines.push('');
  }

  if (opts.mode === 'pro') {
    lines.push('# Redis (required for pro mode)');
    lines.push('REDIS_URL=redis://localhost:6379');
    lines.push('');
  }

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Interactive Onboarding
// ---------------------------------------------------------------------------

async function prompt(rl: ReturnType<typeof createInterface>, question: string): Promise<string> {
  const answer = await rl.question(chalk.bold(question));
  return answer.trim();
}

async function promptChoice(
  rl: ReturnType<typeof createInterface>,
  question: string,
  options: { key: string; label: string }[],
): Promise<string> {
  console.log('');
  console.log(chalk.bold(question));
  for (const opt of options) {
    console.log(`  ${chalk.cyan(opt.key)}) ${opt.label}`);
  }
  const validKeys = options.map(o => o.key);
  while (true) {
    const answer = await prompt(rl, `Choice [${validKeys.join('/')}]: `);
    if (validKeys.includes(answer)) return answer;
    console.log(chalk.red(`  Invalid choice. Options: ${validKeys.join(', ')}`));
  }
}

async function runInteractive(): Promise<void> {
  const banner = figlet.textSync('profClaw', { font: 'Standard' });
  console.log(chalk.cyan(banner));
  console.log(chalk.dim('  Onboarding Wizard\n'));

  // Step 1: Detect environment
  console.log(chalk.bold.white('  Step 1: Environment Detection\n'));
  const spin = spinner('Scanning environment...');
  spin.start();
  const env = await detectEnvironment();
  spin.stop();

  const mark = (ok: boolean) => (ok ? chalk.green('  +') : chalk.dim('  -'));
  console.log(`${mark(true)} OS: ${env.os} (${env.arch})`);
  console.log(`${mark(true)} Node: ${env.nodeVersion}`);
  console.log(`${mark(true)} RAM: ${env.totalMemoryMb} MB`);
  console.log(`${mark(env.isDocker)} Docker: ${env.isDocker ? 'yes' : 'no'}`);
  console.log(`${mark(env.hasRedis)} Redis: ${env.hasRedis ? 'connected' : 'not found'}`);
  console.log(`${mark(env.hasOllama)} Ollama: ${env.hasOllama ? 'running' : 'not found'}`);
  console.log(`${mark(env.hasClaude)} Claude CLI: ${env.hasClaude ? 'found' : 'not found'}`);
  console.log(`${mark(env.hasGit)} Git: ${env.hasGit ? 'found' : 'not found'}`);

  const recommended = recommendMode(env);
  console.log(`\n  ${chalk.cyan('Recommended mode:')} ${chalk.bold(recommended)}`);

  const rl = createInterface({ input: process.stdin, output: process.stdout });

  // Step 2: Choose mode
  console.log(chalk.bold.white('\n  Step 2: Deployment Mode\n'));
  const modeChoice = await promptChoice(rl, '  Select deployment mode:', [
    { key: '1', label: `Pico -- Agent + tools only, ~50MB RAM${recommended === 'pico' ? chalk.green(' (recommended)') : ''}` },
    { key: '2', label: `Mini -- + Dashboard, cron, integrations, ~150MB RAM${recommended === 'mini' ? chalk.green(' (recommended)') : ''}` },
    { key: '3', label: `Pro -- Everything, Redis required, ~300MB RAM${recommended === 'pro' ? chalk.green(' (recommended)') : ''}` },
  ]);
  const mode: DeploymentMode = modeChoice === '1' ? 'pico' : modeChoice === '2' ? 'mini' : 'pro';
  success(`Mode: ${mode}`);

  // Step 3: AI Provider
  console.log(chalk.bold.white('\n  Step 3: AI Provider\n'));
  let anthropicKey: string | undefined;
  let openaiKey: string | undefined;
  let ollamaUrl: string | undefined;

  const hasExistingProvider = !!process.env.ANTHROPIC_API_KEY || !!process.env.OPENAI_API_KEY;
  if (hasExistingProvider) {
    info('AI provider already configured from environment.');
  } else {
    const providerChoice = await promptChoice(rl, '  Select AI provider:', [
      { key: '1', label: 'Anthropic (Claude) -- recommended' },
      { key: '2', label: 'OpenAI (GPT-4o)' },
      { key: '3', label: `Ollama (free, local)${env.hasOllama ? chalk.green(' -- detected!') : ''}` },
      { key: '4', label: 'Skip for now' },
    ]);

    if (providerChoice === '1') {
      anthropicKey = await prompt(rl, '  Anthropic API Key: ');
      if (anthropicKey) success('Anthropic key saved.');
    } else if (providerChoice === '2') {
      openaiKey = await prompt(rl, '  OpenAI API Key: ');
      if (openaiKey) success('OpenAI key saved.');
    } else if (providerChoice === '3') {
      ollamaUrl = env.hasOllama
        ? (process.env.OLLAMA_BASE_URL || 'http://localhost:11434')
        : await prompt(rl, '  Ollama URL [http://localhost:11434]: ') || 'http://localhost:11434';
      success(`Ollama: ${ollamaUrl}`);
    }
  }

  // Step 4: Generate .env
  console.log(chalk.bold.white('\n  Step 4: Configuration\n'));

  if (!env.existingEnv) {
    const envContent = generateEnvFile({
      mode,
      anthropicKey,
      openaiKey,
      ollamaUrl,
    });
    const envPath = join(process.cwd(), '.env');
    writeFileSync(envPath, envContent);
    success('.env file created.');
  } else {
    info('.env file already exists. Updating PROFCLAW_MODE...');
    const envPath = join(process.cwd(), '.env');
    let content = readFileSync(envPath, 'utf-8');
    if (content.includes('PROFCLAW_MODE=')) {
      content = content.replace(/PROFCLAW_MODE=\w+/, `PROFCLAW_MODE=${mode}`);
    } else {
      content += `\nPROFCLAW_MODE=${mode}\n`;
    }
    writeFileSync(envPath, content);
    success(`PROFCLAW_MODE=${mode} set in .env`);
  }

  rl.close();

  // Step 5: Run setup wizard
  console.log(chalk.bold.white('\n  Step 5: Setup\n'));
  info('Launching setup wizard for admin account and provider configuration...\n');

  // Delegate to the setup command
  const { setupCommand } = await import('./setup.js');
  const setupCmd = setupCommand();
  await setupCmd.parseAsync(['node', 'profclaw', 'setup']);
}

// ---------------------------------------------------------------------------
// Non-Interactive Onboarding
// ---------------------------------------------------------------------------

async function runNonInteractive(options: {
  mode?: string;
  provider?: string;
  port?: string;
}): Promise<void> {
  console.log(chalk.dim('  Running onboard in non-interactive mode...\n'));

  const spin = spinner('Detecting environment...');
  spin.start();
  const env = await detectEnvironment();
  spin.stop();

  const mode = (options.mode || recommendMode(env)) as DeploymentMode;
  success(`Mode: ${mode}`);

  // Generate .env if it doesn't exist
  if (!env.existingEnv) {
    const envContent = generateEnvFile({ mode, port: options.port });
    writeFileSync(join(process.cwd(), '.env'), envContent);
    success('.env created');
  }

  // Delegate to setup --non-interactive
  const { setupCommand } = await import('./setup.js');
  const setupCmd = setupCommand();
  const args = ['node', 'profclaw', 'setup', '--non-interactive'];
  if (options.provider) args.push('--ai-provider', options.provider);
  await setupCmd.parseAsync(args);
}

// ---------------------------------------------------------------------------
// Command Registration
// ---------------------------------------------------------------------------

export function onboardCommand(): Command {
  const cmd = new Command('onboard')
    .description('Zero-to-running onboarding wizard (environment detection + setup + start)')
    .option('--non-interactive', 'Run without prompts')
    .option('--mode <mode>', 'Deployment mode: pico, mini, pro')
    .option('--provider <provider>', 'AI provider: anthropic, openai, ollama')
    .option('--port <port>', 'Server port (default: 3000)')
    .action(async (options) => {
      try {
        if (options.nonInteractive) {
          await runNonInteractive({
            mode: options.mode,
            provider: options.provider,
            port: options.port,
          });
        } else {
          await runInteractive();
        }
      } catch (err) {
        error(err instanceof Error ? err.message : 'Onboarding failed');
        process.exit(1);
      }
    });

  return cmd;
}
